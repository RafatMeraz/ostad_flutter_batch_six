main() {
  int a = 23 + 45;
  int b = 23 - 4;
  int c = 454 * 34;
  double d = 34 / 2;
  int e = 23 % 2;
  print(a);
  print(b);
  print(c);
  print(d);
  print(e);

  // a = a + 1;
  print(a++);
  print(a);
  // a = a - 1;
  print(a--);
  print(a);

  print(++a);
  print(--a);

  a += 10; //  a = a + 10;
  a -= 12; // a = a - 12;
  a *= 2; // a = a * 2;
  // a /= 2; // a = a / 2;
  a %= 2; // a = a % 2;
}